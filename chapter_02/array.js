// Array Declaration
let array = [];

// we can put any type of data in array.
array = ["Alex", "Cassy","Shoun","Doerge"];

// print
console.log(array);

// Method.

// (1) length
console.log(array.length);


// (2) join()
console.log(array.join(" "));

// (3) indexOf()
console.log(array.indexOf("Shoun"));

// (4) concat()
console.log(array.concat(["Quill","Peter"]));

// (5) push() - add element to end of array.
console.log(array.push("Groot"));

console.log(array);

// Pop() remove last element from array.
console.log(array.pop("Groot"));